package util;

public class OsagaiaTBX {
	
	private String katea = "";
	private String jatorriOrdaina = "";
	
		public OsagaiaTBX(String katea, String jatorriOrdaina) {
		super();
		this.katea = katea;
		this.jatorriOrdaina = jatorriOrdaina;
	}

		public String getKatea() {
			return katea;
		}

		public void setKatea(String katea) {
			this.katea = katea;
		}

		public String getJatorriOrdaina() {
			return jatorriOrdaina;
		}

		public void setJatorriOrdaina(String jatorriOrdaina) {
			this.jatorriOrdaina = jatorriOrdaina;
		}
		

}
